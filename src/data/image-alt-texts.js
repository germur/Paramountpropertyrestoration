/**
 * Get alt text for an image by filename
 * @param {string} filename - Image filename with or without extension
 * @returns {string} Descriptive alt text
 */
export function getAltText(filename) {
  // Remove file extension and path
  const cleanName = filename
    .split('/').pop()
    .replace(/\.(jpg|jpeg|png|webp|svg)$/i, '');

  // Return mapped alt text or generate generic one
  return imageAltTexts[cleanName] || generateGenericAlt(cleanName);
}

/**
 * Generate generic alt text from filename
 * @param {string} filename - Clean filename without extension
 * @returns {string} Generated alt text
 */
function generateGenericAlt(filename) {
  return filename
    .replace(/([A-Z])/g, ' $1')
    .replace(/[-_]/g, ' ')
    .trim()
    .replace(/\s+/g, ' ')
    .toLowerCase()
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}
